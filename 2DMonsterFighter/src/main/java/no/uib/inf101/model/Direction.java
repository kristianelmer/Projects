package no.uib.inf101.model;

/**
 * Represents the possible movement and attack directions in the game.
 * Used for player movement and attack orientation.
 */
public enum Direction {
    /** Movement or attack in the upward direction */
    UP,
    /** Movement or attack in the downward direction */
    DOWN,
    /** Movement or attack in the leftward direction */
    LEFT,
    /** Movement or attack in the rightward direction */
    RIGHT,
    /** No movement or attack direction */
    NONE;
}
